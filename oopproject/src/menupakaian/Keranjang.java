    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        double ttotal = 0;
            // Mendapatkan jumlah baris dalam tabel
        int rowCount = model.getRowCount();

        // Loop melalui setiap baris tabel
        for (int i = 0; i < rowCount; i++) {
            // Mendapatkan nilai "Total" dari setiap baris
            double nilaiTotal = Double.parseDouble(model.getValueAt(i, 3).toString());
            String nilaiBarang = model.getValueAt(i, 0).toString();

            // Menambahkan nilai "Total" ke variabel total
            ttotal += nilaiTotal;
            daftarBarang.add(nilaiBarang);

        }

        CheckOut pembayaran = new CheckOut(daftarBarang);
        pembayaran.setTotalHarga(ttotal);
        pembayaran.setVisible(true);
        dispose();
        // Sekarang, variabel total berisi jumlah total dari kolom "Total"
        System.out.println("Total: " + ttotal);
    }//GEN-LAST:event_jButton5ActionPerformed

