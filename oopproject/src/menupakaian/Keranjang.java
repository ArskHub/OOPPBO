    public class Barang {
       private String nama;
       private double harga;
       private double total;
       private int jumlah;

       public Barang(String nama, double harga, int jumlah) {
           this.nama = nama;
           this.harga = harga;
           this.jumlah = jumlah;
           this.total = harga * jumlah;
        }

        public String getNama() {
            return nama;
        }

        public double getHarga() {
            return harga;
        }

        public int getJumlah() {
            return jumlah;
        }

        public double getTotal() {
            return total;
        }
        
        public void setJumlah(int jumlah){
            this.jumlah = jumlah;
            jumlah++;
        }
   } 
    public void addDataToTable(String namaBarang,double harga, int jumlah, double total ){
        model.addRow(new Object[]{namaBarang,harga,jumlah,total});
        model.fireTableDataChanged();
    }                    
